    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
      
            String  vdescricao = produto_descricao.getText();
        String vestoque = produto_estoque.getText();
        String vestoqueMinimo = produto_estoque_minimo.getText();
        String vprecoCusto = produto_preco_custo.getText();
        String vDescontoMaximo  = produto_desconto_maximo.getText();
        String vPrecoVenda = produto_preco_venda.getText();
        
        Produto p = new Produto();
        p.setProdutoDescricao(vdescricao);
        p.setProdutoEstoque(Integer.parseInt(vestoque));
        p.setProdutoEstoqueMinimo(Integer.parseInt(vestoqueMinimo));
        p.setProdutoPrecoCusto(Double.parseDouble(vprecoCusto));
        p.setProdutoDesconto(Double.parseDouble(vDescontoMaximo));
        p.setProdutoPrecoVenda(Double.parseDouble(vPrecoVenda));
        p.setProdutoData(Calendar.getInstance());
        
       
        ProdutoDao pdao = new ProdutoDao();    
        pdao.adiciona(p);
      
      
        
        produto_descricao.setText("");
        produto_desconto_maximo.setText("");
        produto_estoque.setText("");
        produto_estoque_minimo.setText("");
        produto_preco_custo.setText("");
        produto_preco_venda.setText("");
        
          
        
    }//GEN-LAST:event_jButton1ActionPerformed

