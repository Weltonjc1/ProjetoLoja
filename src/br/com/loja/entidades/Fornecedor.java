package br.com.loja.entidades;

public class Fornecedor {
	private int fornecedorCodigo;
	private String fornecedorRazaoSocial;
	private String fornecedorNomeFantasia;
	
	
	public int getFornecedorCodigo() {
		return fornecedorCodigo;
	}
	public void setFornecedorCodigo(int fornecedorCodigo) {
		this.fornecedorCodigo = fornecedorCodigo;
	}
	public String getFornecedorRazaoSocial() {
		return fornecedorRazaoSocial;
	}
	public void setFornecedorRazaoSocial(String fornecedorRazaoSocial) {
		this.fornecedorRazaoSocial = fornecedorRazaoSocial;
	}
	public String getFornecedorNomeFantasia() {
		return fornecedorNomeFantasia;
	}
	public void setFornecedorNomeFantasia(String fornecedorNomeFantasia) {
		this.fornecedorNomeFantasia = fornecedorNomeFantasia;
	}
	
	
}
